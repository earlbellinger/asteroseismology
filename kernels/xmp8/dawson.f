C     TO EVALUATE DAWSON'S INTEGRAL 

      PROGRAM DAWINT
      IMPLICIT REAL*8(A-H,O-Z)
 
51    FORMAT('   X =',1PD14.6,5X,'DAWSON(X) =',D14.6)

 
100   PRINT *,'TYPE X    (QUITS WHEN X<-1000)'
      READ *,X
      IF(X.LT.-1000) STOP
      F1=DAWSON(X)
      WRITE(6,51) X,F1
      GO TO 100
      END
 
C     ----------------------------------------------------
 
C	To calculate the Dawson's integral for a real argument
C
C	Required routines : None
 
      FUNCTION DAWSON(X)
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION A(9),B(9),A1(9),B1(10),A2(8),B2(9),A3(7),B3(7)
 
C	Coefficients of rational function approximations
      DATA A/  5.574810327568612837D-01,  1.480548760676749464D-01,
     1         2.473412335012745505D-02,  2.885568838534784757D-03,
     1         2.460310593071530085D-04,  1.551300911064284532D-05,
     1         7.087756590256507931D-07,  2.179833744510066470D-08,
     1         3.580284771465818339D-10/
      DATA B/  1.000000000000000001D+00, -1.091856339098055081D-01,
     1         4.306752089643668630D-02, -1.497994816990499693D-03,
     1         3.339251349042084215D-04, -1.857560947421670758D-06,
     1         6.710614181693725933D-07,  4.960681954907270745D-09,
     1         2.335008164471329286D-10/
 
      DATA A1/  6.262545365722985257D-01,  1.885254515728064716D-01,
     1          3.762507762042703303D-02,  4.987142830383369467D-03,
     1          6.094443149907482678D-04,  3.731642154042092311D-05,
     1          4.239542422752186968D-06,  5.982569991142279706D-08,
     1          1.174473438334967860D-08/
      DATA B1/  1.000651029373811169D+00, -4.210501168492219455D-02,
     1          3.981798325257397695D-02,  1.025635538449943440D-03,
     1          4.099305572015143676D-04,  1.596933865686699852D-05,
     1          2.244388174606844525D-06,  3.090088866526039173D-08,
     1          5.894053468010250910D-09, -1.103344321928670385D-13/
 
      DATA A2/ -4.779664691344904517D+00,  3.275776248263644817D+00,
     1         -8.957008446633805629D-01,  1.364310927775567374D-01,
     1         -1.254364750946865384D-02,  7.203420550034988907D-04,
     1         -2.404381247095368252D-05,  3.964910408777002362D-07/
      DATA B2/ -1.813546033357404877D+00,  1.455784804742178438D+00,
     1         -4.178728305708361334D-01,  6.532985079094380749D-02,
     1         -6.100423692880221490D-03,  3.543095925141692234D-04,
     1         -1.192279451351772837D-05,  1.982456062768223261D-07,
     1         -2.989826117053882457D-16/
 
      DATA A3/ -7.277595909795730480D+01,  2.030133303927516449D+03,
     1         -2.623905050877405481D+04,  1.572452946972041743D+05,
     1         -3.959782020608236881D+05,  3.242235333091555331D+05,
     1         -3.695969139799184010D+04/
      DATA B3/  4.999999999999999987D-01, -3.613797954897864312D+01,
     1          9.972476621892572722D+02, -1.263834541306106681D+04,
     1          7.275923849721134328D+04, -1.668382015827219241D+05,
     1          1.031530728542497468D+05/
 
      XA=ABS(X)
      IF(XA.LT.2.5) THEN
        Y=X*X
        FN=(((((((B(9)*Y+B(8))*Y+B(7))*Y+B(6))*Y+B(5))*Y+B(4))*Y+B(3))*Y
     1           +B(2))*Y+B(1)
        FD=((((((((A(9)*Y+A(8))*Y+A(7))*Y+A(6))*Y+A(5))*Y+A(4))*Y+
     1             A(3))*Y+A(2))*Y+A(1))*Y+1
        DAWSON=X*FN/FD
 
      ELSE IF(XA.LT.4.0) THEN
        Y=X*X
        FN=((((((((B1(10)*Y+B1(9))*Y+B1(8))*Y+B1(7))*Y+B1(6))*Y+B1(5))*Y
     1            +B1(4))*Y+B1(3))*Y+B1(2))*Y+B1(1)
        FD=((((((((A1(9)*Y+A1(8))*Y+A1(7))*Y+A1(6))*Y+A1(5))*Y+A1(4))*Y+
     1             A1(3))*Y+A1(2))*Y+A1(1))*Y+1
        DAWSON=X*FN/FD
 
      ELSE IF(XA.LT.5.5) THEN
        Y=X*X
        FN=(((((((B2(9)*Y+B2(8))*Y+B2(7))*Y+B2(6))*Y+B2(5))*Y
     1           +B2(4))*Y+B2(3))*Y+B2(2))*Y+B2(1)
        FD=(((((((A2(8)*Y+A2(7))*Y+A2(6))*Y+A2(5))*Y+A2(4))*Y+
     1            A2(3))*Y+A2(2))*Y+A2(1))*Y+1
        DAWSON=X*FN/FD
 
      ELSE
        Y=1./X**2
        FN=(((((B3(7)*Y+B3(6))*Y+B3(5))*Y+B3(4))*Y+B3(3))*Y+B3(2))*Y
     1         +B3(1)
        FD=((((((A3(7)*Y+A3(6))*Y+A3(5))*Y+A3(4))*Y+
     1           A3(3))*Y+A3(2))*Y+A3(1))*Y+1
        DAWSON=FN/(FD*X)
      ENDIF
 
      END
